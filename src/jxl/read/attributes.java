package jxl.read;

public class attributes {
	private String id;
	private String name;
	private String command;
	private String lineType;
	private String fulfillmentStatus;
	private String fulfillmentShipementStatus;
	private String fulfillmentTrackingNumber;
	private String fulfillmentSendReceipt;
	
	public attributes() {
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCommand() {
		return command;
	}

	public void setCommand(String command) {
		this.command = command;
	}

	public String getLineType() {
		return lineType;
	}

	public void setLineType(String lineType) {
		this.lineType = lineType;
	}

	public String getFulfillmentStatus() {
		return fulfillmentStatus;
	}

	public void setFulfillmentStatus(String fulfillmentStatus) {
		this.fulfillmentStatus = fulfillmentStatus;
	}

	public String getFulfillmentShipementStatus() {
		return fulfillmentShipementStatus;
	}

	public void setFulfillmentShipementStatus(String fulfillmentShipementStatus) {
		this.fulfillmentShipementStatus = fulfillmentShipementStatus;
	}

	public String getFulfillmentTrackingNumber() {
		return fulfillmentTrackingNumber;
	}

	public void setFulfillmentTrackingNumber(String fulfillmentTrackingNumber) {
		this.fulfillmentTrackingNumber = fulfillmentTrackingNumber;
	}

	public String getFulfillmentSendReceipt() {
		return fulfillmentSendReceipt;
	}

	public void setFulfillmentSendReceipt(String fulfillmentSendReceipt) {
		this.fulfillmentSendReceipt = fulfillmentSendReceipt;
	}
	
	
}

